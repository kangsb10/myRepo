package kr.or.ddit.vo;

public class DiseaseVO {

	private String cat_no;
	private String species_code;
	private String disease_nm;
	private String class_nm;
	private String disease_code;

	public String getCat_no() {
		return cat_no;
	}

	public void setCat_no(String cat_no) {
		this.cat_no = cat_no;
	}

	public String getSpecies_code() {
		return species_code;
	}

	public void setSpecies_code(String species_code) {
		this.species_code = species_code;
	}

	public String getDisease_nm() {
		return disease_nm;
	}

	public void setDisease_nm(String disease_nm) {
		this.disease_nm = disease_nm;
	}

	public String getClass_nm() {
		return class_nm;
	}

	public void setClass_nm(String class_nm) {
		this.class_nm = class_nm;
	}

	public String getDisease_code() {
		return disease_code;
	}

	public void setDisease_code(String disease_code) {
		this.disease_code = disease_code;
	}

}
