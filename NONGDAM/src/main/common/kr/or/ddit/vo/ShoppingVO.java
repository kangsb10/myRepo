package kr.or.ddit.vo;

import java.util.List;

public class ShoppingVO {

	private String rnum;
	private String goods_id; // 상품 아이디
	private String lprod_gu; // 카테고리 코드
	private String buyer_id; // 구매처 아이디
	private String goods_nm; // 상품 명
	private String goods_pc; // 상품 가격
	private String goods_invntry; // 상품 재고량
	private String goods_dc; // 상품 설명

	private List<ShoppingFileItemVO> items;

	
	public String getRnum() {
		return rnum;
	}

	public void setRnum(String rnum) {
		this.rnum = rnum;
	}

	public String getGoods_id() {
		return goods_id;
	}

	public void setGoods_id(String goods_id) {
		this.goods_id = goods_id;
	}

	public String getLprod_gu() {
		return lprod_gu;
	}

	public void setLprod_gu(String lprod_gu) {
		this.lprod_gu = lprod_gu;
	}

	public String getBuyer_id() {
		return buyer_id;
	}

	public void setBuyer_id(String buyer_id) {
		this.buyer_id = buyer_id;
	}

	public String getGoods_nm() {
		return goods_nm;
	}

	public void setGoods_nm(String goods_nm) {
		this.goods_nm = goods_nm;
	}

	public String getGoods_pc() {
		return goods_pc;
	}

	public void setGoods_pc(String goods_pc) {
		this.goods_pc = goods_pc;
	}

	public String getGoods_invntry() {
		return goods_invntry;
	}

	public void setGoods_invntry(String goods_invntry) {
		this.goods_invntry = goods_invntry;
	}

	public String getGoods_dc() {
		return goods_dc;
	}

	public void setGoods_dc(String goods_dc) {
		this.goods_dc = goods_dc;
	}

	public List<ShoppingFileItemVO> getItems() {
		return items;
	}

	public void setItems(List<ShoppingFileItemVO> items) {
		this.items = items;
	}

}
